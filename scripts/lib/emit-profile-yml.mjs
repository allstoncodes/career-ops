// Builds the config/profile.yml object from parsed job-profile.md sections,
// and serializes it with a provenance header. Pure (no filesystem I/O).

import yaml from 'js-yaml';
import {
  parseKeyValueBullets,
  parseGfmTable,
  parseLabeledBullets,
  parseBulletList,
  parseSubsections,
} from './job-profile-parser.mjs';

const stripQuotes = (s) =>
  typeof s === 'string' ? s.replace(/^["']|["']$/g, '').trim() : s;

function requireSection(sections, name) {
  if (!sections.has(name)) {
    throw new Error(
      `job-profile.md missing required "## ${name}". ` +
        `Found: ${[...sections.keys()].join(', ')}`,
    );
  }
  return sections.get(name);
}

function noFill(value, field) {
  if (value == null || value === '' || /<\s*FILL IN/i.test(value)) {
    throw new Error(`<FILL IN> (or empty) in required field "${field}" — complete it in job-profile.md`);
  }
  return value;
}

function subBody(subs, prefix) {
  const s = subs.find((x) => x.heading.startsWith(prefix));
  return s ? s.bodyLines : [];
}
const firstLine = (lines) => lines.map((l) => l.trim()).find((l) => l.length > 0) ?? '';

export function buildProfileObject(sections, { existingCvFormat } = {}) {
  const id = parseKeyValueBullets(requireSection(sections, 'Identity'));
  const targetLines = requireSection(sections, 'Target Roles');
  const archetypeRows = parseGfmTable(requireSection(sections, 'Archetypes'));
  const comp = parseKeyValueBullets(requireSection(sections, 'Compensation'));
  const narrativeSubs = parseSubsections(requireSection(sections, 'Narrative'));
  const proofRows = parseGfmTable(requireSection(sections, 'Proof Points'));

  if (archetypeRows.length === 0) throw new Error('Archetypes table parsed to 0 rows');
  if (proofRows.length === 0) throw new Error('Proof Points table parsed to 0 rows');

  const labeled = parseLabeledBullets(targetLines);
  const headline = firstLine(subBody(narrativeSubs, 'Headline'));

  const obj = {
    candidate: {
      full_name: noFill(id.get('Full name'), 'candidate.full_name'),
      email: noFill(id.get('Email'), 'candidate.email'),
      phone: id.get('Phone') ?? '',
      location: id.get('Location') ?? '',
      linkedin: id.get('LinkedIn URL') ?? '',
      portfolio_url: id.get('Portfolio URL') ?? '',
      github: id.get('GitHub handle') ?? '',
      twitter: id.get('Twitter/X') ?? '',
    },
    target_roles: {
      primary: labeled.get('Primary') ?? [],
      archetypes: archetypeRows.map((r) => ({
        name: r.Archetype,
        level: r.Level,
        fit: r.Fit,
      })),
    },
    narrative: {
      headline: noFill(headline, 'narrative.headline'),
      exit_story: firstLine(subBody(narrativeSubs, 'Exit story')),
      superpowers: parseBulletList(subBody(narrativeSubs, 'Superpowers')),
      proof_points: proofRows.map((r) => ({
        name: r.Project,
        url: r.URL,
        hero_metric: r['Hero metric'],
      })),
    },
    compensation: {
      target_range: noFill(stripQuotes(comp.get('Target total comp')), 'compensation.target_range'),
      currency: 'USD',
      minimum: stripQuotes(comp.get('Minimum walk-away')) ?? '',
      location_flexibility: stripQuotes(comp.get('Location flexibility')) ?? '',
    },
    location: {
      country: 'United States',
      city: id.get('City for resume/outreach') ?? '',
      timezone: 'America/Los_Angeles',
      visa_status: stripQuotes(id.get('Visa status')) ?? '',
    },
    cv: {
      output_format: existingCvFormat ?? 'html',
    },
  };
  return obj;
}

export function serializeProfileYml(obj, date = new Date().toISOString().slice(0, 10)) {
  const header =
    `# config/profile.yml\n` +
    `# Generated by scripts/derive-from-job-profile.mjs on ${date}\n` +
    `# Source of truth: personal-vault/_profiles/job-profile.md — DO NOT hand-edit; re-run the script.\n`;
  return header + yaml.dump(obj, { lineWidth: -1, quotingType: '"', forceQuotes: true, noRefs: true });
}
